namespace Qbo\FlipBook\Model;

use \Magento\Framework\View\Element\UiComponent\DataProvider\DataProviderInterface;
use \Magento\Framework\View\Element\UiComponent\DataProvider\DataProvider as UiDataProvider;
use \Magento\Framework\Api\Search\SearchResultInterface;

/**
 * Custom FlipBook Grid DataProvider
 *
 * @author kasta
 */
class DataProvider extends UiDataProvider implements DataProviderInterface {
    
    /**
     * @param SearchResultInterface $searchResult
     * @return array
     */
    protected function searchResultToOutput(SearchResultInterface $searchResult)
    {
        $arrItems = [];

        $arrItems['items'] = [];
        foreach ($searchResult->getItems() as $item) {
            $itemData = [];
            foreach ($item->getCustomAttributes() as $attribute) {
                $itemData[$attribute->getAttributeCode()] = $attribute->getValue();
            }
            $arrItems['items'][] = $itemData;
        }

        $arrItems['totalRecords'] = $searchResult->getTotalCount();
        
        /**
         * @modification
         * Get full collection withou paging, in order to calculate accurate rating average in admin Grid
         * 
         * @var Qbo\Survey\Model\DataProvider\Reporting
         */
        foreach ($this->reporting->_fullCollection->getItems() as $item) {
            $itemData = [];
            foreach ($item->getCustomAttributes() as $attribute) {
                $itemData[$attribute->getAttributeCode()] = $attribute->getValue();
            }
            $arrItems['items_all'][] = $itemData;
        }
        
        return $arrItems;
    }
}


