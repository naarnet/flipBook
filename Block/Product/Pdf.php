namespace Qbo\FlipBook\Block\Product;

use Magento\Framework\View\Element\Template\Context;

class Pdf extends \Magento\Framework\View\Element\Template
{
    /**
     * @var attribute to set url 
     */
    protected $pdfUrl;
    
    /**
     * @param Context $context
     */
    public function __construct(
        Context $context
    ) {
        parent::__construct($context);
    }

    /**
     * Get Pdf Url
     * @return type
     */
    public function getPdfUrl()
    {
        return $this->pdfUrl;
    }
    
    /**
     * Set Pdf Url
     * @param type $urlpdf
     */
    public function setPdfUrl($urlpdf)
    {
        $this->pdfUrl = $urlpdf;
    }

}
